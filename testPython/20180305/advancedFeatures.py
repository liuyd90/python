#使用迭代查找一个list中最小和最大值，并返回一个tuple：
def findMinAndMax(l):
    if l == []:
        return None, None
    maxNum = l[0] ;
    minNum = l[0];
    for n in l :
        if n > maxNum :
            maxNum = n ;
        elif n < minNum :
            minNum = n ;
    return minNum,maxNum
if findMinAndMax([]) != (None, None):
    print('测试失败!')
elif findMinAndMax([7]) != (7, 7):
    print('测试失败!')
elif findMinAndMax([7, 1]) != (1, 7):
    print('测试失败!')
elif findMinAndMax([7, 1, 3, 9, 5]) != (1, 9):
    print('测试失败!')
else:
    print('测试成功!')

